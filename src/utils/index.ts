export const activeSliderCri = (id: number) => {
    if(id === 0) return 'barr__cir--1';
    if(id === 1) return 'barr__cir--2';
    if(id === 2) return 'barr__cir--3' //-85deg
    if(id === 3) return 'barr__cir--4' //-135deg
    if(id === 4) return 'barr__cir--5' // -176deg
    if(id === 5) return 'barr__cir--6' //225deg
    if(id === 6) return 'barr__cir--7'
    if(id === 7) return 'barr__cir--8'
}